package com.btc.arch.base;

public class ConfigurationError extends Exception {

	public ConfigurationError() {
		super();
	}

	public ConfigurationError(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public ConfigurationError(String arg0) {
		super(arg0);
	}

	public ConfigurationError(Throwable arg0) {
		super(arg0);
	}

	/**
	 * 
	 */
	private static final long serialVersionUID = 1212663601312687707L;

}
